/* Expand the hash table if needed */
static int _dictExpandIfNeeded(dict *d)
{
    /*增量rehash 正在运行 就返回DICT_OK 0*/
    /* Incremental rehashing already in progress. Return. */
    if (dictIsRehashing(d)) return DICT_OK;

    //如果0号hash表为空，那么就以初始大小扩展它
    //DICT_HT_INITIAL_SIZE 的默认值为 4 
    /* If the hash table is empty expand it to the initial size. */
    if (d->ht[0].size == 0) return dictExpand(d, DICT_HT_INITIAL_SIZE);

    
    //如果used（已使用的）大于等于 size(容量)
    //并且 dict_can_resize 全局设置 或者  used/size > dict_force_resize_ratio
    /* If we reached the 1:1 ratio, and we are allowed to resize the hash
     * table (global setting) or we should avoid it but the ratio between
     * elements/buckets is over the "safe" threshold, we resize doubling
     * the number of buckets. */
    //used >= size，即负载因子 used / size >= 1，且 dict_can_resize = 1 允许重哈希时，会触发字典的扩展
    //dict_can_resize 的值，在服务器运行期间可以通过函数被修改的，
    //例如进行子进程操作的时候（bgsave / bgrewrite）的时候
    //会调用 updateDictResizePolicy 来更新它（根据是否有活跃子进程，主要是为了避免 COW 的开销
    //当负载因子大于 5 的时候，不管 dict_can_resize 的值是多少，都会触发扩展，因为负载因子已经过大
    if (d->ht[0].used >= d->ht[0].size &&
        (dict_can_resize ||
         d->ht[0].used/d->ht[0].size > dict_force_resize_ratio))
    {
        //空字典进来会初始化 size 为 4，其他字典如果需要扩展，则会至少扩展为 ht[0].used * 2，翻倍扩容
        ////哈希表的大小总是 2 的乘幂（也即是 2^N，此处 N 未知）
        //1 号哈希表的大小总比 0 号哈希表大
        //2倍的used
        //2.6 还是 return dictExpand(d, ((d->ht[0].size > d->ht[0].used) ?  d->ht[0].size : d->ht[0].used)*2);
        return dictExpand(d, d->ht[0].used*2);
    }
    return DICT_OK;
}

/* Our hash table capability is a power of two */
static unsigned long _dictNextPower(unsigned long size)
{
    unsigned long i = DICT_HT_INITIAL_SIZE;

    if (size >= LONG_MAX) return LONG_MAX + 1LU;
    while(1) {
        // 找到第一个 2的幂 >= size 的，返回它
        if (i >= size)
            return i;
        // 每次都是扩展 2 的幂
        i *= 2; //总是乘以2
    }
}

/* Returns the index of a free slot that can be populated with
 * a hash entry for the given 'key'.
 * If the key already exists, -1 is returned.
 *
 * Note that if we are in the process of rehashing the hash table, the
 * index is always returned in the context of the second (new) hash table. */
static int _dictKeyIndex(dict *d, const void *key)
{
    unsigned int h, idx, table;
    dictEntry *he;

    //先调用 _dictExpandIfNeeded ，检查两个哈希表是否有足够的空间容纳新元素：
    /* Expand the hash table if needed */
    if (_dictExpandIfNeeded(d) == DICT_ERR)
        return -1;
    /* Compute the key hash value */
    h = dictHashKey(d, key);
    for (table = 0; table <= 1; table++) {
        idx = h & d->ht[table].sizemask;
        /* Search if this slot does not already contain the given key */
        he = d->ht[table].table[idx];
        while(he) {
            if (key==he->key || dictCompareKeys(d, key, he->key))
                return -1;
            he = he->next;
        }
        if (!dictIsRehashing(d)) break;
    }
    return idx;
}

void dictEmpty(dict *d, void(callback)(void*)) {
    _dictClear(d,&d->ht[0],callback);
    _dictClear(d,&d->ht[1],callback);
    d->rehashidx = -1;
    d->iterators = 0;
}

